// ─── Derived summaries ─────────────────────────────────────────────────────────

export function toFlashSaleSummary(fs: FlashSale): FlashSaleSummary {
  return {
    flashSaleId:       fs.flashSaleId,
    ten:               fs.ten,
    trangThai:         fs.trangThai,
    batDau:            fs.batDau,
    ketThuc:           fs.ketThuc,
    soLuongPhienBan:   fs.items.length,
    tongSanPhamDaBan:  fs.items.reduce((s, i) => s + i.soLuongDaBan, 0),
    tongGioiHan:       fs.items.reduce((s, i) => s + i.soLuongGioiHan, 0),
    createdAt:         fs.createdAt,
  };
}

